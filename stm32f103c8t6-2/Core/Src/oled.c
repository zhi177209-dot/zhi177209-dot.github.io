void OLED_ShowZh16x16ByIndex(uint8_t x, uint8_t page, uint8_t index)
{
    uint8_t col;

    if (oled_ready == 0U)
    {
        return;
    }
    if (index >= OLED_ZH16X16_COUNT)
    {
        return;
    }
    if (x > 112U || page > 6U)
    {
        return;
    }

    OLED_SetPos(x, page);
    for (col = 0U; col < 16U; col++)
    {
        OLED_WriteData(oled_zh16x16[index][4U + col]);
    }

    OLED_SetPos(x, (uint8_t)(page + 1U));
    for (col = 0U; col < 16U; col++)
    {
        OLED_WriteData(oled_zh16x16[index][20U + col]);
    }
}

void OLED_ShowBMP(uint8_t x, uint8_t page, uint8_t width, uint8_t height, const uint8_t *bmp)
{
    uint8_t p;
    uint8_t col;
    uint8_t page_num;
    uint8_t draw_width;
    uint16_t index = 0U;

    if (oled_ready == 0U || bmp == 0)
    {
        return;
    }
    if (x >= 128U || page >= 8U || width == 0U || height == 0U)
    {
        return;
    }

    page_num = (uint8_t)((height + 7U) / 8U);
    draw_width = width;
    if ((uint16_t)x + draw_width > 128U)
    {
        draw_width = (uint8_t)(128U - x);
    }

    for (p = 0U; p < page_num; p++)
    {
        if ((uint8_t)(page + p) >= 8U)
        {
            break;
        }

        OLED_SetPos(x, (uint8_t)(page + p));
        for (col = 0U; col < draw_width; col++)
        {
            OLED_WriteData(bmp[index++]);
        }

        index = (uint16_t)(index + (uint16_t)(width - draw_width));
    }
}

void OLED_ShowDemoBMP(uint8_t x, uint8_t page)
{
    OLED_ShowBMP(x, page, OLED_DEMO_BMP_WIDTH, OLED_DEMO_BMP_HEIGHT, oled_demo_bmp);
}

void OLED_ShowNum8x16(uint8_t x, uint8_t page, int16_t num, uint8_t decimal_bits)
{
    static const uint16_t dec_scale[] = {1U, 10U, 100U, 1000U};
    char text[12];
    char int_buf[6];
    uint8_t i;
    uint8_t text_len = 0U;
    uint8_t int_len = 0U;
    int32_t value = num;
    uint16_t scale;
    uint16_t frac_div;
    uint16_t frac_part;
    uint32_t int_part;

    if (oled_ready == 0U || page >= 7U || x > 120U || decimal_bits > 3U)
    {
        return;
    }

    if (value < 0)
    {
        text[text_len++] = '-';
        value = -value;
    }

    scale = dec_scale[decimal_bits];
    int_part = (uint32_t)value / scale;
    frac_part = (uint16_t)((uint32_t)value % scale);

    if (int_part == 0U)
    {
        int_buf[int_len++] = '0';
    }
    else
    {
        while (int_part > 0U && int_len < sizeof(int_buf))
        {
            int_buf[int_len++] = (char)('0' + (int_part % 10U));
            int_part /= 10U;
        }
    }

    while (int_len > 0U)
    {
        text[text_len++] = int_buf[--int_len];
    }

    if (decimal_bits > 0U)
    {
        text[text_len++] = '.';
        frac_div = (uint16_t)(scale / 10U);

        while (frac_div > 0U && text_len < sizeof(text))
        {
            text[text_len++] = (char)('0' + (frac_part / frac_div));
            frac_part %= frac_div;
            frac_div /= 10U;
        }
    }

    for (i = 0U; i < text_len; i++)
    {
        uint8_t glyph_index;

        if ((uint16_t)x + (uint16_t)(i * 8U) > 120U)
        {
            break;
        }

        if (text[i] >= '0' && text[i] <= '9')
        {
            glyph_index = (uint8_t)(text[i] - '0');
        }
        else if (text[i] == '.')
        {
            glyph_index = 10U;
        }
        else if (text[i] == '-')
        {
            glyph_index = 11U;
        }
        else
        {
            continue;
        }

        OLED_ShowGlyph8x16((uint8_t)(x + i * 8U), page, oled_num8x16[glyph_index]);
    }
}

void OLED_ShowSingleNum8x16(uint8_t x, uint8_t page, uint8_t ch)
{
    if (oled_ready == 0U || ch > 11U || page >= 7U || x > 120U)
    {
        return;
    }

    OLED_ShowGlyph8x16(x, page, oled_num8x16[ch]);
}
