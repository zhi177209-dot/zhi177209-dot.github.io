static HAL_StatusTypeDef OLED_WriteByte(uint8_t ctrl, uint8_t value)
{
    uint8_t tx_buf[2] = {ctrl, value};

    if (oled_ready == 0U)
    {
        oled_last_i2c_status = HAL_ERROR;
        return oled_last_i2c_status;
    }

    oled_last_i2c_status = HAL_I2C_Master_Transmit(&hi2c1, oled_i2c_addr, tx_buf, 2, 100);
    if (oled_last_i2c_status != HAL_OK)
    {
        oled_ready = 0U;
    }

    return oled_last_i2c_status;
}

static void OLED_SetPos(uint8_t x, uint8_t page)
{
    OLED_WriteCmd((uint8_t)(0xB0U + page));
    OLED_WriteCmd((uint8_t)(x & 0x0FU));
    OLED_WriteCmd((uint8_t)(0x10U | (x >> 4)));
}

void OLED_WriteCmd(uint8_t cmd)
{
    (void)OLED_WriteByte(OLED_CMD_CTRL, cmd);
}

void OLED_WriteData(uint8_t dat)
{
    (void)OLED_WriteByte(OLED_DATA_CTRL, dat);
}

static void OLED_ShowGlyph8x16(uint8_t x, uint8_t page, const uint8_t glyph[16])
{
    uint8_t c;

    if (oled_ready == 0U || page >= 7U || x > 120U)
    {
        return;
    }

    OLED_SetPos(x, page);
    for (c = 0U; c < 8U; c++)
    {
        OLED_WriteData(glyph[c]);
    }

    OLED_SetPos(x, (uint8_t)(page + 1U));
    for (c = 0U; c < 8U; c++)
    {
        OLED_WriteData(glyph[c + 8U]);
    }
}

void OLED_Clear(void)
{
    uint8_t page;
    uint8_t col;

    if (oled_ready == 0U)
    {
        return;
    }

    for (page = 0U; page < 8U; page++)
    {
        OLED_SetPos(0U, page);
        for (col = 0U; col < 128U; col++)
        {
            OLED_WriteData(0x00U);
        }
    }
}

uint8_t OLED_Init(void)
{
    static const uint16_t probe_addrs[] = {OLED_DEV_ADDR_GND, OLED_DEV_ADDR_VCC};
    uint8_t i;

    oled_ready = 0U;
    oled_last_i2c_status = HAL_OK;

    HAL_Delay(100);

    for (i = 0U; i < (uint8_t)(sizeof(probe_addrs) / sizeof(probe_addrs[0])); i++)
    {
        if (HAL_I2C_IsDeviceReady(&hi2c1, probe_addrs[i], 2, 50) == HAL_OK)
        {
            oled_i2c_addr = probe_addrs[i];
            oled_ready = 1U;
            break;
        }
    }

    if (oled_ready == 0U)
    {
        oled_last_i2c_status = HAL_ERROR;
        return 0U;
    }

    OLED_WriteCmd(0xAE);
    OLED_WriteCmd(0xD5);
    OLED_WriteCmd(0x80);
    OLED_WriteCmd(0xA8);
    OLED_WriteCmd(0x3F);
    OLED_WriteCmd(0xD3);
    OLED_WriteCmd(0x00);
    OLED_WriteCmd(0x40);
    OLED_WriteCmd(0x8D);
    OLED_WriteCmd(0x14);
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x02);
    OLED_WriteCmd(0xA1);
    OLED_WriteCmd(0xC8);
    OLED_WriteCmd(0xDA);
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0x81);
    OLED_WriteCmd(0xCF);
    OLED_WriteCmd(0xD9);
    OLED_WriteCmd(0xF1);
    OLED_WriteCmd(0xDB);
    OLED_WriteCmd(0x30);
    OLED_WriteCmd(0xA4);
    OLED_WriteCmd(0xA6);
    OLED_WriteCmd(0xAF);

    if (oled_ready == 0U)
    {
        return 0U;
    }

    OLED_Clear();
    return 1U;
}

